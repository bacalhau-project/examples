// GetAllEmojis returns a single slice containing all emojis
func GetAllEmojis() []string {
	var allEmojis []string
	for _, emojis := range emojiCategories {
		allEmojis = append(allEmojis, emojis...)
	}
	return allEmojis
}

// GetRandomEmojiFromCategory returns a random emoji from a specific category
func GetRandomEmojiFromCategory(r *rand.Rand, category string) string {
	if emojis, ok := emojiCategories[category]; ok {
		return emojis[r.Intn(len(emojis))]
	}
	// Default to geometric if category not found
	return emojiCategories["Geometric"][r.Intn(len(emojiCategories["Geometric"]))]
}

// GetRandomEmoji returns a random emoji from any category
func GetRandomEmoji(r *rand.Rand) string {
	// Get all categories
	categories := make([]string, 0, len(emojiCategories))
	for category := range emojiCategories {
		categories = append(categories, category)
	}

	// Pick a random category
	randomCategory := categories[r.Intn(len(categories))]
	return GetRandomEmojiFromCategory(r, randomCategory)
}
